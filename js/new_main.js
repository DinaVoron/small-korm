function makeList(arr) {
    html = '<ul>\r\n';
    for (let i = 0; i < arr.length - 1; i++) {
        html += `<li>${arr[i]};</li>\r\n`;
    }
    html += `<li>${arr[arr.length - 1]}.</li>\r\n`;
    html += '<ul>\r\n';
    return html;
}

function printTable(arr) {
	let html = ``;
	html += `<table id="KL">
            <tr>
                <th></th>
                <th>НАЗВАНИЕ КОРМА</th>
                <th>СОСТАВ</th>
                <th>ПРОЦЕНТ СОДЕРЖАНИЯ МЯСА</th>
                <th>ЦЕНА ЗА КИЛОГРАММ</th>
            </tr>`;
	for (let i = 0; i < arr.length; i++) {
		html += `<tr>`;
		html += `<td><img src="${arr[i]['pict']}"></td>`;
		html += `<td>${arr[i]["name"]}</td>`;
		html += `<td>${makeList(arr[i]["compound"])}</td>`;
		html += `<td>${arr[i]["procent"]}</td>`;
		html += `<td>${arr[i]["price"]}</td>`;
		html += `</tr>`;
	}
	return html;
}

