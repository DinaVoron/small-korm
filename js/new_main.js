function sortTable(arrKorm) {
    if (priceSort !== '' || meetSort !== '') {
        arrKorm.sort((elem1, elem2) => {
            if (elem1.price !== elem2.price && priceSort !== '') {
                if (priceSort === 'hight') {
                    return (elem1.price > elem2.price) ? 1 : -1;
                }
                if (priceSort === 'low') {
                    return (elem1.price < elem2.price) ? 1 : -1;
                }
            } 
            else {
                if (elem1.procent !== elem2.procent) {
                    if (meetSort === 'hight') {
                        return (elem1.procent > elem2.procent) ? 1 : -1;
                    }
                    if (meetSort === 'low') {
                        return (elem1.procent < elem2.procent) ? 1 : -1;
                    }
                    return 0;
                } else {
                    return 0;
                }
            }
	    })
    }
}

function searchFilter(arr, filter) {
    for (elem in arr) {
        if (arr[elem].indexOf(filter) !== -1) {
            return true;
        }
    }
    return false;
}

function filter(arr) {
    if (compFilter === '') {
        return arr;
    }
	let newKorm = [];
    for (let i = 0; i < arr.length; i++) {
        if (!searchFilter(arr[i]["compound"], compFilter)) {
            newKorm.push(arr[i]);
        }
    }
	return newKorm;
}


